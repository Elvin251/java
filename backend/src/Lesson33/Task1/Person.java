package Lesson33.Task1;

public class Person {
    private String ad;
    private int yas;
    private String email;

    public String getAd() {
        return ad;
    }
    public void setAd(String ad) {
        this.ad = ad;
    }

    public int getYas() {
        return yas;
    }
    public void setYas(int yas) {
        this.yas = yas;
    }

    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
}
